Map::Map() {

	dirt = TextureManager::LoadTexture("assets/dirt.png");
	grass = TextureManager::LoadTexture("assets/grass.png");
	water = TextureManager::LoadTexture("assets/water.png");

	loadMap(lvl1);

	src.x = src.y = 0;
	src.w = dest.w = 32;
	src.h = dest.h = 32;

	dest.x = dest.y = 0;
}


void Map::loadMap(int arr[28][50]) {

	for (int row = 0; row < 28; row++) {
		for (int col = 0; col < 50; col++) {
			map[row][col] = arr[row][col];
		}
	}
}


void Map::drawMap() {

	int type = 0;

	for (int row = 0; row < 28; row++) {
		for (int col = 0; col < 50; col++) {
			type = map[row][col];
			dest.x = col * 32;
			dest.y = row * 32;

			switch (type) {
			case 0:
				TextureManager::drawTex(water, src, dest);
				break;
			case 1:
				TextureManager::drawTex(grass, src, dest);
				break;
			case 2:
				TextureManager::drawTex(dirt, src, dest);
				break;
			default:
				break;
			}
		}
	}
}

